<div class="textblock"><p>NRE-Math is the mathematical module used in NearlyRealEngine</p>
<p>It regroup simple mathematical class and a generic implementation for international unit system.</p>
<h2>Features</h2>
<ul>
<li>Header-only</li>
<li><p class="startli">Vector 2D, 3D, 4D :</p>
<p class="startli">Vector regroup all methods to describe a cartesian 2D, 3D, 4D vector. 3D &amp; 4D Vector represent 2D &amp; 3D homogeneous Vector.</p>
<p class="startli">All bases operations are supported : +, -, *, /, dot product (|), cross product (^), ==, !=, &gt;, &lt;, &gt;=, &lt;=</p>
<p class="startli">Normalisation, distance computation, individual elements access (getter or [ ]).</p>
</li>
<li><p class="startli">Point 2D, 3D, 4D :</p>
<p class="startli">Simple aliases on Vector</p>
</li>
<li><p class="startli">Matrix 3x3, 4x4 :</p>
<p class="startli">Allow simple use to create transformation matrix.</p>
<p class="startli">All bases operations are supported : +, -, *, /, ==, !=</p>
<p class="startli">Possible transformations :</p><ul>
<li>Translation</li>
<li>Rotation</li>
<li>Sheer</li>
<li>Scale</li>
<li>Squeeze</li>
<li>Perspective</li>
<li>Orthogonal</li>
</ul>
<p class="startli">Transformations can be apply by multiplying the matrix with a supported object :</p><ul>
<li>Vector</li>
<li>Point</li>
<li>Plane</li>
<li>Segment</li>
<li>BezierCurve</li>
</ul>
</li>
<li><p class="startli">Segment 3D, 4D :</p>
<p class="startli">Represent 2D &amp; 3D homogeneous Segments</p>
</li>
<li><p class="startli">BezierCurve 3D, 4D :</p>
<p class="startli">Represent 2D &amp; 3D homogeneous Bezier Curves</p>
</li>
<li><p class="startli">Frustum :</p>
<p class="startli">Represent a view frustum, used in collaboration with camera to set up a perspective camera</p>
</li>
<li><p class="startli">Plane :</p>
<p class="startli">Represent a 3D plane, it's use 3D homogeneous coordinates to allow simple transformations with a matrix</p>
</li>
<li><p class="startli">Quaternion :</p>
<p class="startli">Represent a simple quaternion, can be converted to a 4x4 matrix in order to represent a rotation matrix.</p>
</li>
</ul>
